package Exercise.Chapter1;

public class Ex_1_1_8 {
    public static void DoExercise()
    {
        // 单引号和双引号的区别是
        // 单引号的返回值是char
        // 双引号的返回值是String

        System.out.println('b');
        System.out.println('b' + 'c'); // 98 + 99 = 197
        System.out.println((char)('a') + 4); // 97 + 4
    }
}

